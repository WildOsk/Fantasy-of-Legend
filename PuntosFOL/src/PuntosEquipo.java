    private int puntuacionJugador(int posicion, int kills, int muertes, int asistencias, int minions, int triples, int cuadras, int pentas, boolean vision) {
        int puntuacion = 0;

        if (vision) {
            puntuacion++;
        }
        if (muertes == 0) {
            puntuacion += 2;
        } else if (muertes >= 5) {
            puntuacion -= (muertes / 5) * 2;
        }
        if (asistencias >= 10) {
            puntuacion += 2;
            puntuacion += (asistencias - 10) / 5;
        }
        puntuacion += triples * 2 + cuadras * 5 + pentas * 10;
        puntuacion -= muertes;
        if (posicion != 5) {
            puntuacion += kills * 2 + asistencias;
            if (posicion != 2) {
                BigDecimal pMin = new BigDecimal(minions * 0.01).setScale(0, RoundingMode.HALF_UP);
                System.out.print(pMin);
                puntuacion += pMin.intValue();
            } else {
                BigDecimal pMin = new BigDecimal(minions * 0.015).setScale(0, RoundingMode.HALF_UP);
                puntuacion += pMin.intValue();
            }
        } else {
            puntuacion += kills * 1.5 + asistencias * 1.5;
            BigDecimal pMin = new BigDecimal(minions * 0.02).setScale(0, RoundingMode.HALF_UP);
            puntuacion += pMin.intValue();
        }

        return puntuacion;
    }

