    private int puntuacionEntrenador(int dragones, int ancianos, int heraldos, int barones, int aces, int torres, boolean primeraSangre, boolean ganado, boolean vmin, boolean tmin){
        int total = 0;


        total += dragones*2;
        if(dragones == 4){
            total += 1;
        }
        if(ancianos>0){
            total+=3;
            total += (ancianos-1)*2;
        }
        total += heraldos*2 + barones*4;
        total += aces*3;
        total += torres;
        if(primeraSangre){
            total+=2;
        }
        if(ganado){
            total+=5;
        } else {
            total -= 2;
        }
        if(vmin){
            total += 7;
        } else if(tmin){
            total += 3;
        }

        return total;
    }
    
